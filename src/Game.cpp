WorldGrid Game::world(w);

Player Game::player;

Game::Game()
{
    for (int i = 0; i < 6; i++)
    {
        int x, y;
        bool validLoc = false;
        Enemy e(rand() % 4, rand() % 5 + 5);

        while (!validLoc)
        {
            x = rand() % 28 + 1;
            y = rand() % 28 + 1;

            if (world.getTile(x, y).isWalkable())
                validLoc = true;
        }

        enemyGrid.add(e, x, y);
    }

    dropReady = false;
}

Game::~Game()
{
    //dtor
}

void Game::dropItems(time_t curTime)
{
    if (!dropReady && pickupManager.size() == 0)
    {
        dropTime = curTime;
        nextDrop = rand() % 15 % 15;

        bool validLoc = false;
        int x, y;

        while (!validLoc)
        {
            x = rand() % 28 + 1;
            y = rand() % 28 + 1;

            if (world.getTile(x, y).isWalkable())
                validLoc = true;
        }

        pickupManager.add(Pickup(rand() % 3, x, y));

        dropReady = true;
    }
    else if (difftime(curTime, dropTime) > nextDrop && player.moved)
    {
        nextDrop = rand() % 15 % 15;
        dropReady = false;
        pickupManager.place(0);
    }
}

void Game::eventHandle(Event event)
{
    player.eventHandle(event);
}

void Game::Draw(RenderWindow& window)
{
    world.Draw(window);
    enemyGrid.Draw(window);

    if (pickupManager.placed)
        pickupManager.Draw(window);

    if (player.getHealth() > 0)
        player.Draw(window);
}

void Game::Update()
{
    time(&timer);
    dropItems(timer);

    enemyGrid.Update();

    if (player.getHealth() <= 0)
    {
        player.x = 100;
        player.y = 100;
    }

    player.moved = false;
}
